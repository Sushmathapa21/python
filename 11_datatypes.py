#datatypes:

#int
i = 3021
print(type(i))

#str #string ma space ley ni euta character ko space khanchhha.
s = "Sushma"
print(type(s))

#float #.2f le chei . pachi duita matrai digit linchha
f = 3147.3021
print(type(f))

#list #changeable
s = [3,0,2,1]
print(type(s))

#tuple #unchangeable
t = (3,1,4,7)
print(type(t))

#dict
d = {"name": "Sushma", "num" : 3021}
print(type(d))

#set
set = {"apple", "banana", "Mango"}
print(type(set))

#frozenset
fset = frozenset({"chocolate", "strawberry", "vanilla"})
print(type(fset))

#bool
sushma_is_a_good_girl = True
print(type(sushma_is_a_good_girl))

#nonetype
n = None
print(type(n))

#range
num = range(5)
print(type(num))

#complex:
z = 5 + 2j
print(type(z))

